/* =========================================================
   تنظیمات Firebase — این فایل باید با اطلاعات پروژه خودتان پر شود
   =========================================================

   چرا این فایل لازم است؟
   وقتی سایت روی GitHub Pages منتشر می‌شود، فقط فایل‌های استاتیک
   (HTML/CSS/JS) اجرا می‌شوند و هیچ سرور یا دیتابیسی وجود ندارد.
   Firebase Firestore یک دیتابیس رایگان ابری است که مستقیماً از
   داخل مرورگر (بدون نیاز به سرور) قابل استفاده است، بنابراین
   وقتی شما یک مطلب جدید ثبت می‌کنید، همه بازدیدکننده‌های سایت
   (روی هر مرورگر و هر دستگاهی) آن را می‌بینند.

   راه‌اندازی (حدود ۵ دقیقه، کاملاً رایگان):

   ۱) به آدرس https://console.firebase.google.com بروید و با
      حساب گوگل خود وارد شوید.

   ۲) روی «Add project» بزنید و یک نام برای پروژه انتخاب کنید
      (مثلا mrshortfilm). Google Analytics را می‌توانید غیرفعال
      بگذارید (لازم نیست).

   ۳) از منوی سمت راست، بخش Build > Firestore Database را باز
      کنید و روی «Create database» بزنید.
      - یک منطقه (Location) نزدیک انتخاب کنید (مثلا eur3).
      - در قدمِ Rules، گزینه «Start in test mode» را انتخاب کنید
        تا فعلا خواندن/نوشتن آزاد باشد (نکته امنیتی پایین همین
        فایل را هم بخوانید).

   ۴) از منوی سمت راست روی آیکن چرخ‌دنده (Project settings) بزنید.
      در پایین صفحه، بخش «Your apps» را ببینید و روی آیکن وب
      ( </> ) کلیک کنید تا یک اپ وب ثبت کنید. یک نام دلخواه
      بدهید و «Register app» را بزنید.

   ۵) کدی شبیه زیر به شما نشان داده می‌شود؛ مقادیر داخل آن را
      (نه ساختار fireabseConfig را) در پایین همین فایل جایگزین
      کنید:

      const firebaseConfig = {
        apiKey: "AIzaSy...",
        authDomain: "mrshortfilm-xxxx.firebaseapp.com",
        projectId: "mrshortfilm-xxxx",
        storageBucket: "mrshortfilm-xxxx.appspot.com",
        messagingSenderId: "1234567890",
        appId: "1:1234567890:web:abcdef123456"
      };

   ۶) فایل را ذخیره کنید، تغییرات را روی گیت‌هاب (git push) بفرستید.
      GitHub Pages خودش سایت را به‌روزرسانی می‌کند.

   ۷) وارد blog-admin.html شوید؛ اگر بالای فرم پیام سبز رنگ
      «اتصال به Firebase برقرار است» را دیدید، یعنی همه‌چیز درست
      کار می‌کند و از این به بعد مطالب برای همه نمایش داده می‌شوند.

   -----------------------------------------------------------
   نکته امنیتی (مهم):
   حالت «Test mode» یعنی هرکسی که آدرس سایت شما را دارد می‌تواند
   از طریق کنسول مرورگر مستقیماً در دیتابیس بنویسد یا حذف کند؛
   برای یک وبلاگ شخصی معمولاً مشکلی ایجاد نمی‌کند، ولی اگر
   می‌خواهید امن‌تر باشد، بعد از راه‌اندازی اولیه، در Firestore
   Database > Rules می‌توانید قوانین را محدودتر کنید، مثلا فقط
   اجازه خواندن عمومی بدهید و نوشتن را به یک رمز ساده یا به
   Firebase Authentication محدود کنید. اگر خواستید این بخش
   امنیتی را هم برایتان تنظیم کنم، کافی است بگویید.
   ========================================================= */


