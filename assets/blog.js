/* ---------- سئوی کامل صفحه تک‌مطلب (متا، Open Graph، تویتر، Schema.org) ---------- */
function absoluteUrl(path) {
    try {
        return new URL(path, window.location.href).toString();
    } catch (e) {
        return path;
    }
}

function stripHtml(str) {
    return String(str || '').replace(/<[^>]*>/g, '');
}

function applySeoMeta(post, id) {
    const desc = stripHtml((post.seo && post.seo.metaDescription) || post.summary || (post.content || '').split(/\n+/)[0] || '').slice(0, 300);
    const keywords = (post.seo && post.seo.keywords) || ['ماندالا', 'بهنام خسروی', post.category, 'فیلم کوتاه', 'مستر شورت‌فیلم'].filter(Boolean).join(', ');
    const pageUrl = absoluteUrl('blog-post.html?id=' + encodeURIComponent(id));
    const imageUrl = post.image ? absoluteUrl(post.image) : absoluteUrl('Images/mandala.jpg');

    const setAttr = (elId, attr, value) => {
        const el = document.getElementById(elId);
        if (el) el.setAttribute(attr, value);
    };

    setAttr('meta-description', 'content', desc);
    setAttr('meta-keywords', 'content', keywords);
    setAttr('canonical-link', 'href', pageUrl);

    setAttr('og-title', 'content', post.title);
    setAttr('og-description', 'content', desc);
    setAttr('og-image', 'content', imageUrl);
    setAttr('og-url', 'content', pageUrl);

    setAttr('tw-title', 'content', post.title);
    setAttr('tw-description', 'content', desc);
    setAttr('tw-image', 'content', imageUrl);

    const jsonLdEl = document.getElementById('post-jsonld');
    if (jsonLdEl) {
        const jsonLd = {
            '@context': 'https://schema.org',
            '@type': 'NewsArticle',
            headline: post.title,
            description: desc,
            image: [imageUrl],
            datePublished: post.date || '',
            dateModified: post.date || '',
            author: { '@type': 'Organization', name: 'تیم مستر شورت‌فیلم' },
            publisher: {
                '@type': 'Organization',
                name: 'مستر شورت‌فیلم',
                logo: { '@type': 'ImageObject', url: absoluteUrl('Images/mb.png') }
            },
            mainEntityOfPage: { '@type': 'WebPage', '@id': pageUrl },
            articleSection: post.category || ''
        };
        jsonLdEl.textContent = JSON.stringify(jsonLd);
    }
}

